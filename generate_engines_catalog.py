# ─────────────────────────────────────────────────────────────
#  Parts that depend on fuel type (for JS filtering)
# ─────────────────────────────────────────────────────────────
DIESEL_INCOMPATIBLE = {
    "Свечи зажигания", "Катушки зажигания",
    "Высоковольтные провода", "Модуль зажигания",
    "Распределитель зажигания",
}
DIESEL_ONLY = {"Свечи накаливания"}
ELECTRIC_INCOMPATIBLE = {
    "Свечи зажигания", "Катушки зажигания", "Высоковольтные провода",
    "Топливный насос", "Топливный фильтр", "Топливная форсунка",
    "Масляный насос", "Масляный фильтр", "Масляный радиатор",
    "Свечи накаливания", "Выхлопная труба", "Глушитель",
    "Каталитический нейтрализатор", "Ремень ГРМ", "Цепь ГРМ",
}


def main():
    with open("data/parts_catalog.json", encoding="utf-8") as f:
        cat = json.load(f)

    # Add engines to models
    models_updated = 0
    for brand in cat["brands"]:
        bname = brand["name"]
        brand_engines = ENGINES.get(bname, {})
        if not brand_engines:
            continue
        for model in brand["models"]:
            mname = model["name"]
            if mname in brand_engines:
                model["engines"] = brand_engines[mname]
                models_updated += 1

    # Add fuel_filter metadata to parts_template
    for part in cat["parts_template"]:
        pname = part["name"]
        if pname in DIESEL_INCOMPATIBLE:
            part["fuel_filter"] = ["Бензин", "Гибрид"]
        elif pname in DIESEL_ONLY:
            part["fuel_filter"] = ["Дизель"]
        elif pname in ELECTRIC_INCOMPATIBLE:
            part["not_for"] = ["Электро"]
        else:
            part.pop("fuel_filter", None)
            part.pop("not_for", None)

    oem_lookup = {}
    for (prefix, code), numbers in OEM_LOOKUP.items():
        oem_lookup.setdefault(prefix, {})[code] = numbers
    cat["oem_lookup"] = oem_lookup
    cat["last_updated"] = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    with open("data/parts_catalog.json", "w", encoding="utf-8") as f:
        json.dump(cat, f, ensure_ascii=False, indent=2)

    total_models = sum(len(b["models"]) for b in cat["brands"])
    engines_count = sum(
        len(m.get("engines", []))
        for b in cat["brands"] for m in b["models"]
    )
    print(f"Updated {models_updated}/{total_models} models with engines")
    print(f"Total engine variants: {engines_count}")
    brands_done = sorted(set(
        b["name"] for b in cat["brands"]
        if any("engines" in m for m in b["models"])
    ))
    print(f"Brands with engines ({len(brands_done)}): {', '.join(brands_done)}")


if __name__ == "__main__":
    main()
