def E(vol, cc, fuel, hp, code=""):
    """Create an engine dict."""
    kw = round(hp / 1.35962)
    s = re.sub(r"[^a-z0-9]", "-", f"{vol.replace('.', '-')}-{fuel[:3].lower()}-{hp}")
    if code:
        c = re.sub(r"[^a-z0-9]", "", code.lower())[:8]
        s += f"-{c}"
    label = f"{vol} л {fuel} {hp} л.с." + (f" ({code})" if code else "")
    return {
        "slug": s,
        "volume": vol,
        "volume_cc": cc,
        "fuel": fuel,
        "power_hp": hp,
        "power_kw": kw,
        "code": code,
        "label": label,
    }


