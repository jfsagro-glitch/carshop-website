def expand_catalog():
    """Return only verified extended catalog entries (no synthetic expansion)."""
    return EXTENDED_OEM_CATALOG

def save_extended_catalog():
    """Save extended catalog to JSON"""
    catalog = expand_catalog()
    
    output = {}
    for brand, codes in catalog.items():
        output[brand] = {}
        for code, numbers in codes.items():
            output[brand][code] = numbers
    
    Path("data").mkdir(exist_ok=True)
    with open("data/extended_oem_catalog.json", "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)
    
    total = sum(len(n) for b in output.values() for n in b.values() if isinstance(n, list))
    print(f"Saved extended catalog: {len(output)} brands, {total} OEM entries")
    
    return output

