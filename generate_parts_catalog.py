def make_oem(prefix: str, model_slug: str, part_code: str, year: int) -> str | None:
    """Return a real OEM catalog number when available."""
    key = (prefix, part_code)
    if key in OEM_LOOKUP:
        options = OEM_LOOKUP[key]
        # Deterministic pick based on model slug so the same model always gets the same number
        idx = int(hashlib.md5(model_slug.encode()).hexdigest(), 16) % len(options)
        return options[idx]
    return None

def price_for(category: str, brand: str) -> tuple[float, float]:
    grp = category if category in PRICE_RANGES else "ТО"
    lo, hi = PRICE_RANGES[grp]
    # Premium brands markup
    if brand in ("BMW", "Mercedes-Benz", "Audi", "Lexus", "Porsche"):
        lo = int(lo * 1.5)
        hi = int(hi * 1.8)
    price_usd = round(random.uniform(lo, hi), 2)
    price_kgs = round(price_usd * USD_TO_KGS, 0)
    return price_usd, price_kgs

def compatible_cars_str(brand: str, model: dict) -> str:
    """Build 'Brand Model YEAR-YEAR' compatible string."""
    years = sorted(model.get("years", []))
    if not years:
        return f"{brand} {model['name']}"
    return f"{brand} {model['name']} {min(years)}-{max(years)}"

def generate_records(catalog: dict, limit: int | None = None) -> list[dict]:
    records: list[dict] = []
    seen_oem: set[str] = set()

    for brand_data in catalog["brands"]:
        bname = brand_data["name"]
        prefix = brand_data.get("prefix", bname[:2].upper())

        for model in brand_data.get("models", []):
            mname = model["name"]
            mslug = model["slug"]
            years = sorted(model.get("years", []))
            year_range = f"{min(years)}-{max(years)}" if years else ""
            # Limit years to recent 5 to avoid explosion
            recent_years = years[:5] if years else [2020]
            compatible = compatible_cars_str(bname, model)

            engines = model.get("engines", [])

            for part in catalog["parts_template"]:
                # Skip electric parts for non-electric engines (if noted)
                not_for = part.get("not_for", [])
                if engines and not_for:
                    all_fuels = {e.get("fuel","") for e in engines}
                    if "Электро" in not_for and all_fuels == {"Электро"}:
                        continue

                category = part.get("group", part.get("category", "Прочее"))
                name = f"{part['name']} {bname} {mname}"
                if year_range:
                    name += f" ({year_range})"

                # Use first recent year for OEM seed
                seed_year = recent_years[0] if recent_years else 2020
                oem = make_oem(prefix, mslug, part["code"], seed_year)
                if not oem:
                    continue

                # Deduplicate
                if oem in seen_oem:
                    oem += f"-{mslug[:3].upper()}"
                seen_oem.add(oem)

                price_usd, price_kgs = price_for(category, bname)

                # stock: some in stock, some on order
                stock = random.choices([0, 0, 0, 1, 2, 3, 5], weights=[30,15,10,15,10,10,10])[0]

                records.append({
                    "oem_number": oem,
                    "name": name,
                    "name_ru": name,
                    "brand": bname,
                    "category": category,
                    "models": [f"{bname} {mname}"],
                    "years_from": min(years) if years else None,
                    "years_to": max(years) if years else None,
                    "price_usd": price_usd,
                    "price_kgs": price_kgs,
                    "stock_qty": stock,
                    "is_available": stock > 0,
                    "images": [],
                    "description": (
                        f"Запчасть {part['name']} для {bname} {mname}. "
                        f"Категория: {category}. OEM: {oem}."
                    ),
                })

                if limit and len(records) >= limit:
                    return records

    return records

def delete_generated_parts() -> None:
    """Remove previously generated rows before a full generated catalog import."""
    if not SUPABASE_URL or not SUPABASE_SERVICE_KEY:
        return
    headers = {
        "apikey": SUPABASE_SERVICE_KEY,
        "Authorization": f"Bearer {SUPABASE_SERVICE_KEY}",
        "Prefer": "return=minimal",
    }
    url = f"{SUPABASE_URL}/rest/v1/parts?description=like.%D0%97%D0%B0%D0%BF%D1%87%D0%B0%D1%81%D1%82%D1%8C*"
    resp = requests.delete(url, headers=headers, timeout=60)
    resp.raise_for_status()
    print("Deleted previous generated parts rows")


def import_to_supabase(records: list[dict], batch_size: int = 200) -> tuple[int, int]:
    if not SUPABASE_URL or not SUPABASE_SERVICE_KEY:
        print("ERROR: SUPABASE_URL / SUPABASE_SERVICE_KEY not configured", file=sys.stderr)
        return 0, len(records)

    headers = {
        "apikey": SUPABASE_SERVICE_KEY,
        "Authorization": f"Bearer {SUPABASE_SERVICE_KEY}",
        "Content-Type": "application/json",
        "Prefer": "return=minimal",
    }
    url = f"{SUPABASE_URL}/rest/v1/parts"

    ok = err = 0
    for i in range(0, len(records), batch_size):
        batch = records[i : i + batch_size]
        resp = requests.post(url, json=batch, headers=headers)
        if resp.status_code in (200, 201):
            ok += len(batch)
            print(f"  Imported batch {i//batch_size + 1}: {len(batch)} records OK")
        else:
            err += len(batch)
            print(f"  Batch {i//batch_size + 1} ERROR {resp.status_code}: {resp.text[:200]}")
    return ok, err

def save_csv(records: list[dict], path: str):
    fields = ["oem_number","name","name_ru","brand","category","models","years_from","years_to",
              "price_usd","price_kgs","stock_qty","is_available","images","description"]
    with open(path, "w", encoding="utf-8-sig", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fields, extrasaction="ignore")
        w.writeheader()
        w.writerows(records)
    print(f"Saved {len(records)} records to {path}")

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run", action="store_true", help="Generate CSV but don't import")
    parser.add_argument("--limit", type=int, default=None, help="Max records to generate")
    parser.add_argument("--output", default="generated_parts_catalog.csv", help="Output CSV path")
    parser.add_argument("--append", action="store_true", help="Append generated rows instead of replacing previous generated rows")
    args = parser.parse_args()

    print("Loading catalog...")
    catalog = json.load(open("data/parts_catalog.json", encoding="utf-8"))

    print("Generating records...")
    records = generate_records(catalog, limit=args.limit)
    print(f"Generated {len(records)} records")

    save_csv(records, args.output)

    if args.dry_run:
        print("Dry run — skipping Supabase import")
        return

    print(f"\nImporting to Supabase...")
    if not args.append:
        delete_generated_parts()
    ok, err = import_to_supabase(records)
    print(f"\nDone: {ok} imported, {err} errors")

    # Also import original 20-row CSV
    print("\nImporting original parts_catalog.csv...")
    orig_raw = list(csv.DictReader(open("parts_catalog.csv", encoding="utf-8-sig")))
    orig = []
    for r in orig_raw:
        # Map CSV fields to Supabase schema
        cc = r.get("compatible_cars","")
        models_list = [m.strip() for m in cc.replace("|",",").split(",") if m.strip()] if cc else []
        orig.append({
            "oem_number": r.get("oem_number",""),
            "name": r.get("name",""),
            "name_ru": r.get("name",""),
            "brand": r.get("brand",""),
            "category": r.get("category",""),
            "models": models_list,
            "price_usd": float(r["price_usd"]) if r.get("price_usd") else None,
            "price_kgs": float(r["price_kgs"]) if r.get("price_kgs") else None,
            "stock_qty": int(r["stock_qty"]) if r.get("stock_qty") else 0,
            "is_available": int(r.get("stock_qty","0") or 0) > 0,
            "images": [],
            "description": r.get("description",""),
        })
    ok2, err2 = import_to_supabase(orig)
    print(f"Original CSV: {ok2} imported, {err2} errors")

if __name__ == "__main__":
    main()
